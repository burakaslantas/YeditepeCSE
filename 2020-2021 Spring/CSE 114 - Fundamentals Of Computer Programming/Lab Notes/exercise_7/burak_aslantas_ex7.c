#include <stdio.h>

void ReverseString(char *str)
{
    char *start = str, *end = str, temp;

    while(*(end + 1) != '\0')
    {
        end++;
    }
    while(start < end)
    {
        temp = *start;
        *start = *end;
        *end = temp;
        start++;
        end--;
    }
}

int main()
{
    char str[10];
    printf("Enter a string: ");
    fgets(str, sizeof(str), stdin);
    ReverseString(str);
    printf("Reverse of the string: %s", str);
}
